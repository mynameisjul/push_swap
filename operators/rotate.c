#include "../push_swap.h"

void	rotate(t_pile *p)
{
	int		tmp;
	size_t	i;

	i = 1;
	tmp = p->tab[0];
	while (i < p->len)
	{
		p->tab[i - 1] = p->tab[i];
		i++;
	}
	p->tab[i - 1] = tmp;
}
