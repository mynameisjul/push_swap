#include "../push_swap.h"

void rrotate(t_pile *p)
{
	int		tmp;
	size_t	i;

	i = p->len - 1;
	tmp = p->tab[i];
	while (i > 0)
	{
		p->tab[i] = p->tab[i - 1];
		i--;
	}
	p->tab[0] = tmp;
}
