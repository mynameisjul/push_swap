#include "push_swap.h"

void push(int **src, int **dst)
{
	if (*src)
	{
		if (*dst)
			*dst = ft_rrealloc(1);
		if (!*dst)
			return (0);
		*dst[-1] = *src[0];
		rrotate(dst);
	}

}
